def _weighted_distance(c1, c2):
    dr = c1[0] - c2[0]
    dg = c1[1] - c2[1]
    db = c1[2] - c2[2]
    return 2 * dr * dr + 4 * dg * dg + 3 * db * db


def _nearest_novation(rgb):
    best_idx = 0
    best_dist = float("inf")
    for i, color in enumerate(NOVATION_COLORS):
        d = _weighted_distance(rgb, color)
        if d < best_dist:
            best_dist = d
            best_idx = i
    return best_idx


def ableton_to_novation(ableton_index: int) -> int:
    """Map an Ableton color index (0–69) to the nearest Novation palette index (0–127)."""
    if ableton_index < 0 or ableton_index >= len(ABLETON_COLORS):
        return 0
    rgb = ABLETON_COLORS[ableton_index]
    return _nearest_novation(rgb)


def rgb_to_novation(r: int, g: int, b: int) -> int:
    """Map an arbitrary RGB tuple to the nearest Novation palette index."""
    return _nearest_novation((r, g, b))


class ColorMapper:
    """Stateless helper wrapping module-level functions."""

    def ableton_to_novation(self, ableton_index: int) -> int:
        return ableton_to_novation(ableton_index)

    def rgb_to_novation(self, r: int, g: int, b: int) -> int:
        return rgb_to_novation(r, g, b)
